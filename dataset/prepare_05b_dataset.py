def process_file(input_path: Path, output_path: Path, extras: list[dict] | None = None) -> dict:
    """Обработать один JSONL файл."""
    results = []
    removed = 0
    merged = 0
    stripped = 0

    with open(input_path, encoding="utf-8") as f:
        for line in f:
            line = line.strip()
            if not line:
                continue
            example = json.loads(line)
            processed = process_example(example)
            if processed is None:
                removed += 1
                continue
            results.append(processed)
            stripped += 1

    # Добавить дополнительные примеры (только в train)
    if extras:
        results.extend(extras)

    # Записать
    with open(output_path, "w", encoding="utf-8") as f:
        for r in results:
            f.write(json.dumps(r, ensure_ascii=False) + "\n")

    return {
        "input": str(input_path.name),
        "output": str(output_path.name),
        "original": removed + stripped,
        "kept": stripped,
        "removed": removed,
        "extras": len(extras) if extras else 0,
        "total": len(results),
    }


def analyze_output(path: Path) -> dict:
    """Анализ итогового датасета."""
    types = {}
    total = 0
    with open(path, encoding="utf-8") as f:
        for line in f:
            d = json.loads(line)
            total += 1
            for m in d["messages"]:
                if m["role"] == "assistant":
                    match = re.search(r'"document_type"\s*:\s*"([^"]+)"', m["content"])
                    if match:
                        t = match.group(1)
                        types[t] = types.get(t, 0) + 1

    return {"total": total, "types": len(types), "distribution": types}


