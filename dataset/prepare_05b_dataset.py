def load_extra_examples() -> list[dict]:
    """Загрузить дополнительные примеры из JSONL файла."""
    extras_path = Path(__file__).parent / "extra_05b_examples.jsonl"
    examples = []
    if extras_path.exists():
        with open(extras_path, encoding="utf-8") as f:
            for line in f:
                line = line.strip()
                if line:
                    examples.append(json.loads(line))
    return examples


