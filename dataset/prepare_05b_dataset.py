# ── Утилиты ───────────────────────────────────────────────────────────────────

def extract_doc_type(content: str) -> str | None:
    """Достать document_type из контента ассистента."""
    m = re.search(r'"document_type"\s*:\s*"([^"]+)"', content)
    return m.group(1) if m else None


def strip_tool_call(content: str) -> str:
    """Убрать <tool_call>...</tool_call> обёртку, оставить чистый JSON."""
    # Убираем XML теги
    cleaned = re.sub(r'</?tool_call>\s*', '', content).strip()

    # Парсим JSON и извлекаем arguments (без regex — надёжнее)
    try:
        obj = json.loads(cleaned)
        if isinstance(obj, dict) and "arguments" in obj:
            return json.dumps(obj["arguments"], ensure_ascii=False)
        return json.dumps(obj, ensure_ascii=False)
    except json.JSONDecodeError:
        pass

    return cleaned


def apply_merge(doc_type: str) -> str:
    """Применить маппинг объединения типов."""
    return MERGE_MAP.get(doc_type, doc_type)


def should_remove(doc_type: str) -> bool:
    """Проверить, нужно ли удалить этот тип."""
    merged = apply_merge(doc_type)
    return merged in REMOVE_TYPES


def update_doc_type_in_json(json_str: str, old_type: str, new_type: str) -> str:
    """Заменить document_type в JSON строке."""
    return json_str.replace(f'"document_type": "{old_type}"', f'"document_type": "{new_type}"')


def process_example(example: dict) -> dict | None:
    """Обработать один пример. Вернуть None если нужно удалить."""
    messages = example["messages"]

    assistant_msg = next((m for m in messages if m["role"] == "assistant"), None)
    if not assistant_msg:
        return None

    content = assistant_msg["content"]
    doc_type = extract_doc_type(content)

    if not doc_type:
        return None

    # Проверяем удаление (до маппинга)
    if should_remove(doc_type):
        return None

    # Применяем маппинг
    new_type = apply_merge(doc_type)

    # Убираем tool_call обёртку
    clean_content = strip_tool_call(content)

    # Обновляем document_type если изменился
    if new_type != doc_type:
        clean_content = update_doc_type_in_json(clean_content, doc_type, new_type)

    # Собираем новый пример
    new_messages = []
    for m in messages:
        if m["role"] == "system":
            new_messages.append({"role": "system", "content": SYSTEM_PROMPT_05B})
        elif m["role"] == "assistant":
            new_messages.append({"role": "assistant", "content": clean_content})
        else:
            new_messages.append(m)

    return {"messages": new_messages}


