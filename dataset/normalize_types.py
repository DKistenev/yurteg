def normalize(doc_type: str) -> str:
    """Возвращает нормализованный тип или '__DROP__' если нужно удалить."""
    return NORMALIZATION_MAP.get(doc_type, doc_type)


def main():
    dataset_dir = Path(__file__).parent
    input_file = dataset_dir / "labeled_data_reviewed.jsonl"
    output_file = dataset_dir / "labeled_data_normalized.jsonl"
    report_file = dataset_dir / "normalize_report.txt"

    total = 0
    kept = 0
    dropped = 0
    changed = defaultdict(list)  # старый → [новый, ...]

    with open(input_file, encoding="utf-8") as f_in, \
         open(output_file, "w", encoding="utf-8") as f_out:

        for line in f_in:
            line = line.strip()
            if not line:
                continue

            total += 1
            record = json.loads(line)

            output = record.get("output", {})
            if isinstance(output, str):
                try:
                    output = json.loads(output)
                except Exception:
                    output = {}

            old_type = output.get("document_type", "")
            new_type = normalize(old_type)

            if new_type == "__DROP__":
                dropped += 1
                changed[old_type].append("__DROP__")
                continue

            if new_type != old_type:
                changed[old_type].append(new_type)
                output["document_type"] = new_type
                record["output"] = output

            kept += 1
            f_out.write(json.dumps(record, ensure_ascii=False) + "\n")

    # Отчёт
    report_lines = [
        f"Всего записей:   {total}",
        f"Оставлено:       {kept}",
        f"Удалено (DROP):  {dropped}",
        "",
        "Изменения типов:",
    ]
    for old, news in sorted(changed.items()):
        unique_news = list(dict.fromkeys(news))
        for new in unique_news:
            if new == "__DROP__":
                report_lines.append(f"  УДАЛЁН:  {old}")
            else:
                report_lines.append(f"  {old}  →  {new}")

    report_text = "\n".join(report_lines)
    report_file.write_text(report_text, encoding="utf-8")

    print(report_text)
    print(f"\nГотово: {output_file}")


if __name__ == "__main__":
    main()
