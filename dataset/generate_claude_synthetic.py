    with open(OUTPUT_SFT, "w", encoding="utf-8") as f:
        for r in sft_records:
            f.write(json.dumps(r, ensure_ascii=False) + "\n")

    print(f"SFT: {len(sft_records)} записей → {OUTPUT_SFT}")

    # === DPO ===
    dpo_records = []
    for item in DPO_DATA:
        text, chosen, rejected = item
        dpo_records.append(dpo_record(text, chosen, rejected))

    # Split DPO: 80/20
    random.shuffle(dpo_records)
    val_size = max(1, len(dpo_records) // 5)
    dpo_val = dpo_records[:val_size]
    dpo_train = dpo_records[val_size:]

    with open(OUTPUT_DPO_TRAIN, "w", encoding="utf-8") as f:
        for r in dpo_train:
            f.write(json.dumps(r, ensure_ascii=False) + "\n")

    with open(OUTPUT_DPO_VAL, "w", encoding="utf-8") as f:
        for r in dpo_val:
            f.write(json.dumps(r, ensure_ascii=False) + "\n")

    print(f"DPO train: {len(dpo_train)} пар → {OUTPUT_DPO_TRAIN}")
    print(f"DPO val:   {len(dpo_val)} пар → {OUTPUT_DPO_VAL}")

